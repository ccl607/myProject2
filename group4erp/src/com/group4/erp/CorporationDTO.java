package com.group4.erp;

public class CorporationDTO {
	
	String corp_no;
	String corp_name;
	String ceo_name;
	String corp_addr;
	String corp_fax;
	String corp_tel;
	String corp_business_area;
	
	
	public String getCorp_no() {
		return corp_no;
	}
	
	public void setCorp_no(String corp_no) {
		this.corp_no = corp_no;
	}
	
	public String getCorp_name() {
		return corp_name;
	}
	
	public void setCorp_name(String corp_name) {
		this.corp_name = corp_name;
	}
	
	public String getCeo_name() {
		return ceo_name;
	}
	
	public void setCeo_name(String ceo_name) {
		this.ceo_name = ceo_name;
	}
	
	public String getCorp_addr() {
		return corp_addr;
	}
	
	public void setCorp_addr(String corp_addr) {
		this.corp_addr = corp_addr;
	}
	
	public String getCorp_fax() {
		return corp_fax;
	}
	
	public void setCorp_fax(String corp_fax) {
		this.corp_fax = corp_fax;
	}
	
	public String getCorp_tel() {
		return corp_tel;
	}
	
	public void setCorp_tel(String corp_tel) {
		this.corp_tel = corp_tel;
	}
	
	public String getCorp_business_area() {
		return corp_business_area;
	}
	
	public void setCorp_business_area(String corp_business_area) {
		this.corp_business_area = corp_business_area;
	}

}
